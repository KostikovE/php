<?php
declare(strict_types=1);

/**
 * Вывод строки HELLO посимвольно в столбик
 * 
 * Создается переменная $var со значением 'HELLO'
 * Используется цикл while для перебора каждого символа строки
 * Каждый символ выводится на отдельной строке с тегом <br>
 */

// Создание переменной и присвоение строкового значения
$var = "HELLO";

$i = 0;

/**
 * Цикл while для перебора каждого символа строки
 * Условие: пока счетчик меньше длины строки
 */
while ($i < strlen($var)) {
    // Перенос строки для браузера
    echo $var[$i] . "<br>";
    
    // Увеличение счетчика
    $i++;
}
?>